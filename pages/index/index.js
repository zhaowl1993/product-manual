  // 切换产品
  toggleProduct(e) {
    const productId = e.currentTarget.dataset.id;
    this.setData({
      activeProduct: productId,
      showDirectory: false // Hide directory after selecting a product
    });
    
    // For the three special items, scroll to them directly
    if (productId === '药香同源' || productId === '优劣对比' || productId === '保养注意') {
      // Give some time for the directory to close
      setTimeout(() => {
        // Find the element by ID
        const query = wx.createSelectorQuery();
        query.select(`#${productId}`).boundingClientRect();
        query.selectViewport().scrollOffset();
        query.exec((res) => {
          console.log('Query result:', res);
          if (res && res[0]) {
            wx.pageScrollTo({
              scrollTop: res[0].top + res[1].scrollTop,
              duration: 300
            });
          } else {
            console.error(`Element with ID ${productId} not found`);
            // Fallback: try to find by class and text content
            this.scrollToProductByName(productId);
          }
        });
      }, 300);
    }
  },
  
  // Fallback function to scroll to product by name
  scrollToProductByName(productName) {
    console.log(`Trying to find product by name: ${productName}`);
    const query = wx.createSelectorQuery();
    query.selectAll('.product-title').boundingClientRect();
    query.selectViewport().scrollOffset();
    query.exec((res) => {
      if (res && res[0]) {
        // Find the title element that contains the product name
        const targetElement = res[0].find(item => 
          item.dataset && item.dataset.id === productName || 
          (item.id === productName)
        );
        
        if (targetElement) {
          wx.pageScrollTo({
            scrollTop: targetElement.top + res[1].scrollTop,
            duration: 300
          });
        } else {
          console.error(`Could not find product with name: ${productName}`);
        }
      }
    });
  },

