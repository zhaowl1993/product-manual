  // 滚动到指定章节
  scrollToSection(e) {
    const productId = e.currentTarget.dataset.product;
    const sectionTitle = e.currentTarget.dataset.section;
    
    console.log(`需要滚动到的产品和章节: ${productId} - ${sectionTitle}`);
    
    // 关闭目录
    this.setData({
      showDirectory: false
    });
    
    setTimeout(() => {
      // 先查找所有section元素
      wx.createSelectorQuery()
        .selectAll('.section')
        .boundingClientRect(rects => {
          if (!rects || !rects.length) {
            console.error('没有找到任何section元素');
            return;
          }
          
          console.log(`找到 ${rects.length} 个section元素`);
          
          // 查找匹配的section元素
          const targetSection = rects.find(rect => 
            rect.dataset && 
            rect.dataset.product === productId && 
            rect.dataset.section === sectionTitle
          );
          
          if (targetSection) {
            console.log('找到目标section:', targetSection);
            
            // 获取当前页面滚动位置
            wx.createSelectorQuery()
              .selectViewport()
              .scrollOffset(scrollRes => {
                // 计算目标滚动位置
                const targetScrollTop = targetSection.top + scrollRes.scrollTop;
                
                console.log('滚动到位置:', targetScrollTop);
                
                // 执行滚动
                wx.pageScrollTo({
                  scrollTop: targetScrollTop,
                  duration: 300,
                  success: () => console.log('滚动成功'),
                  fail: (err) => console.error('滚动失败:', err)
                });
              })
              .exec();
          } else {
            console.error(`未找到匹配的section: ${productId} - ${sectionTitle}`);
            console.log('所有section的dataset:', rects.map(r => r.dataset));
          }
        })
        .exec();
    }, 600); // 增加延迟，确保目录已完全关闭
  },

